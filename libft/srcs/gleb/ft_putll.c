#include "libft.h"

void	ft_putll(long long n)
{
	long long min;

	min = -9223372036854775807;
	if (n == min)
	{
		write(1, "-9223372036854775808", 20);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
	}
	if ((n / 10) != 0)
		ft_putll(n / 10);
	ft_putchar_fd((n % 10) + '0', 1);
}
